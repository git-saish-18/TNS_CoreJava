package mypackages;

import java.util.Scanner;

public class AssignmentOperators {

	public static void main(String[] args) {
		Scanner cin=new Scanner(System.in);
		int num1=cin.nextInt();
		int num2;
		num2=num1;
		System.out.println(num2);
		num2-=num1;
		System.out.println(num2);
		num2+=num1;
		System.out.println(num2);
		num2*=num1;
		System.out.println(num2);
		

	}

}
