package mypackages;


public class BitwiseOperator {

	public static void main(String[] args) {
	int num1=6;
	int num2=8;
	System.out.println(num1&num2);
	System.out.println(num1|num2);
	System.out.println(num1^num2);
	System.out.println(num1>>1);
	System.out.println(num1<<1);
	System.out.print(num1>num2?true:false);
	
	}

}
